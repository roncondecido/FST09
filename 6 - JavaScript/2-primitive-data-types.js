// Primitive Data Type
// String: textual data enclosed in '' or ""
let firstName = "John";
console.log("Firstname:", firstName);

// typeof operator:
console.log("Data Type:", typeof firstName);

// Number: represents numeric value (3, 3.14, -3)
let age = 30;
console.log("Age:", age);
console.log("Data Type:", typeof age);

// Booleans: either true or false
let isStudent = false;
console.log("Is Student:", isStudent);
console.log("Data Type:", typeof isStudent);

// Null: Intentional absence of value.
let car = null;
console.log("Car:", car);
console.log("Data Type:", typeof car);
car = "Toyota";
console.log("Car:", car);
console.log("Data Type:", typeof car);

// Undefined: Unintentional absence of a value.
let city;
console.log("City:", city);
console.log("Data Type:", typeof city);